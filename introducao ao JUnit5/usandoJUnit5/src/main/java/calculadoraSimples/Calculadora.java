package calculadoraSimples;

/**
 *
 * @author jony_
 */
public class Calculadora {
    
    public double soma(double parcela1, double parcela2){
        return parcela1 + parcela2;
    }
    
    public double subtracao(double minuendo, double subtraendo){
        return minuendo - subtraendo;
    }
    
    public double multiplicacao(double fator1, double fator2){
        return fator1 * fator2;
    }
    
    public double divisao(double dividendo, double divisor){
        return dividendo / divisor;
    }
    
}
